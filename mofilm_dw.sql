CREATE DEFINER=`root`@`localhost` PROCEDURE `DimWaktuProc` (IN `tahun` INT, IN `jumlah_hari` INT)  BEGIN
  SET @loopvar := 1;
  SET @endloopvar := jumlah_hari;
  SET @tglawal = CONCAT(tahun,'-01-01');
 
  DROP TABLE IF EXISTS dim_waktu_temp;
 
  CREATE TABLE dim_waktu_temp(idDate INT, date DATE);
 
  read_loop: LOOP
    IF @loopvar > @endloopvar THEN
        LEAVE read_loop;
    ELSE
        INSERT INTO dim_waktu_temp(idDate, date) VALUES (@loopvar, @tglawal + INTERVAL (@loopvar-1) DAY);
        SET  @loopvar := @loopvar + 1;
    END IF;
  END LOOP;
 
  SELECT *
        , DATE_FORMAT(date, "%Y") AS `year`
        , MONTH(date) AS `month`
        , CASE MONTH(date)
                WHEN 1 THEN 'Januari'
                WHEN 2 THEN 'Februari'
                WHEN 3 THEN 'Maret'
                WHEN 4 THEN 'April'
                WHEN 5 THEN 'Mei'
                WHEN 6 THEN 'Juni'
                WHEN 7 THEN 'Juli'      
                WHEN 8 THEN 'Agustus'  
                WHEN 9 THEN 'September'        
                WHEN 10 THEN 'Oktober'  
                WHEN 11 THEN 'November'        
                WHEN 12 THEN 'Desember'        
          END AS `month_name`
        , DAYOFMONTH(date) AS `day_number`
        , CASE DAYOFWEEK(date)
                WHEN 1 THEN 'Minggu'
                WHEN 2 THEN 'Senin'
                WHEN 3 THEN 'Selasa'
                WHEN 4 THEN 'Rabu'
                WHEN 5 THEN 'Kamis'
                WHEN 6 THEN 'Jumat'
                WHEN 7 THEN 'Sabtu'
          END AS `day_name`
        FROM dim_waktu_temp;
  DROP TABLE IF EXISTS dim_waktu_temp;
END$$

